#include "libft_test.h"

void test_atoi()
{
	char	*a[] = {
		" -sfecf",
		"",
		"+2798",
		"+0089",
		"a56",
		"      --s8",
		"0001020304",
		"000000000000000110",
		"-153",
		"+132",
		"++876",
		"--132",
		"dwbk ",
		" 1",
		"42jk ",
		" 21",
		"     32 ",
		"\n          42 24",
		"1-2",
		"4+2",
		"  +442",
		"  -4232",
		"4,5",
		"+",
		"-",
		"-+1",
		"+-1",
		"\200123",
		"123\200",
		"  \t\n  \r\r\v\f-899",
		"-2147483648",
		"2147483647",
		NULL
	};

	for (int i = 0; a[i]; i++) {
		MU_ASSERT(ft_atoi(a[i]) == atoi(a[i]), \
				  "test (%d) and ctrl (%d) differ (input:%s)", ft_atoi(a[i]), atoi(a[i]), a[i]);
	}
}
