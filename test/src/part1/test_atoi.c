#include "header.h"

int test_atoi(void)
{
	int		i = 0;
	char	*a[] = 
		{ 
			" -sfecf",
			"",
			"+2798",
			"+0089",
			"a56",
			"      --s8",
			"0001020304",
			"000000000000000110",
			"-153",
			"+132",
			"++876",
			"--132",
			"dwbk ",
			" 1",
			"42jk ",
			" 21",
			"     32 ",
			"\n          42 24",
			"1-2",
			"4+2",
			"  +442",
			"  -4232",
			"4,5",
			"+",
			"-",
			"-+1",
			"+-1",
			"\200123",
			"123\200",
			"  \t\n  \r\r\v\f-899",
			"-2147483648",
			"2147483647",
			NULL
		};

	intro("atoi");

	while (a[i])
	{
		if (ft_atoi(a[i]) != atoi(a[i]))
			return (ko_int(__LINE__, a[i], ft_atoi(a[i]), atoi(a[i])));
		i++;
	}
	
	return (ok());
}
